<script setup lang="ts">
import { ref } from 'vue';
import router from './router'
import { useI18n } from 'vue-i18n'

const { t, availableLocales } = useI18n({ useScope: 'global' })

import { RouterLink, RouterView } from 'vue-router'
//import HelloWorld from './components/HelloWorld.vue'

//Build navigation options
const navItems = ref([
	{
		label: "nav.home",
		//icon: 'pi pi-link',
		command: () => {
			router.push('/');
		}
	},
	{
		label: "nav.about",
		//icon: 'pi pi-link',
		command: () => {
			router.push('/about');
		}
	},
]);

//Get available locales
const locales = [];
for (const x in availableLocales) {
	const locale = availableLocales[x];
	locales.push({
		label: locale.toUpperCase(),
		value: locale,
	});
}
</script>


<template>
	<!--header>
		<h1>{{ t("nav.dictionary") }}</h1>
	</header-->

	<Menubar :model="navItems">
		<template #item="{ item, props }">
			<a :href="item.url" :target="item.target" v-bind="props.action">
				<!--span :class="item.icon" /-->
				<span>{{ t(item.label) }}</span>
			</a>
		</template>
		<template #end>
			<img src="@/assets/language.svg" alt="Language" />
			<Select
				v-model="$i18n.locale"
				:options="locales"
				optionLabel="label"
				optionValue="value"
			></Select>
		</template>
	</Menubar>

	<RouterView />

